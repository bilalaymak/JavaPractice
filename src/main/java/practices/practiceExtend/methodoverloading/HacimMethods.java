package practices.practiceExtend.methodoverloading;

public class HacimMethods {


    public int hacimHesapla(int a){
        return a*a*a;
    }
    public int hacimHesapla(int a, int b){
        return a*a*b;
    }
    public int hacimHesapla(int a, int b, int c){
        return a*b*c;
    }








}










