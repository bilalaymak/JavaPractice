package practices.practiceExtend.objectmethodcreation.objectmethodcreation;

public class Calculator {

    //iki variable'in degerlerini cıkarma yapan bir method olusturalım

    public static void cikarma(int a, int b){
        System.out.println("iki sayinin farki : "+(a-b));

    }
    public static void bolme(int a, int b){
        System.out.println("iki sayinin bolme sonucu : "+(a/b));
    }
    public static void carpma(int a, int b){
        System.out.println("iki sayini carpımı : "+(a*b));
    }
    public static void toplama(int a, int b){
        System.out.println("iki sayini toplamı : "+(a+b));

    }


}
