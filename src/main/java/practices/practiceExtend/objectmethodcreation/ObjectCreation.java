package practices.practiceExtend.objectmethodcreation;

public class ObjectCreation {
    /*
    Java'da, object bir class'tan örneklenmiş bir nesnedir
    ve method ise class'ta tanımlanan bir işlevdir.

Class, bir veri yapısı ve işlevlerin bir kümesini tanımlar.
Örneğin, bir "Kitap" class'ı, bir kitap için gerekli olan verileri
(örneğin, kitap adı, yazarı, basım tarihi) ve bu verilere ulaşmak için gerekli olan işlevleri
(örneğin, getBookName(), setAuthor()) içerebilir. Class, bir veri türü olarak düşünülebilir.
Object ise, bir class'tan örneklenmiş bir nesnedir. Örneğin,
"Kitap" class'ından örneklenmiş bir object, bir kitap nesnesi
olacaktır ve bu nesne, "Kitap" class'ında tanımlanan verileri
ve işlevleri içerecektir. Objectler, verileri ve işlevleri
barındıran gerçek örneklerdir.
Method ise, bir class'ta tanımlanan bir işlevdir. Örneğin,
"Kitap" class'ında "getBookName()" methodu, bir kitap nesnesinin
adını döndüren bir işlevdir. Methodlar, class'ta tanımlanan verilere
ve objectlere ulaşmak için kullanılır ve class'ta tanımlanan işlemleri gerçekleştirir.
Özetle, objectler class'tan örneklenen gerçek nesnelerdir
ve methodlar ise class'ta tanımlanan işlevlerdir.

     */
}
